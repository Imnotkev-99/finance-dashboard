--  Para las políticas RLS primero ELIMINA cualquier política previa
--  de `expenses` (incluida "Own read") y luego recrea un set canónico
--  equivalente. El comportamiento de seguridad es el mismo: cada
--  usuario sólo ve/gestiona sus propios gastos.
